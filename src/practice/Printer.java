package practice;

public class Printer {
	
	void println(int x) {
		System.out.println(x);
	} // println
	
	void println(boolean x) {
		System.out.println(x);
	} // println
	
	void println(double x) {
		System.out.println(x);
	} // println
	
	void println(String x) {
		System.out.println(x);
	} // println
	
} // end class
